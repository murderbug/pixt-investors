									<h3 class="color">Pixt is offering friends, family and angel investors the opportunity to subscribe to convertible loan, rather than equity investing. Below are the main reasons that explain the choice.</h3>
									
									<p class="clearfix">&nbsp;</p>
									<h4 class="color">Disadvantages of Equity.</h4>
									<p><strong>Valuation.</strong><br />It is very difficult for an emerging company to calculate an accurate valuation for its  business.  Moreover, angel investors and strategic partners also tend to be unsophisticated when it comes to valuing technology businesses.  Having a valuation is central to an equity financing, as the buyers of stock expect to know precisely what percentage of the company their investment will purchase.</p>
									<p>This would not be an issue if there was only going to be a single financing, but the truth is that emerging companies rarely succeed on a single round of financing.  The next step after raising angel capital is to raise a “Series A Round” from institutional venture capitalists.  VCs have much more experience in valuing technology  companies – and with few exceptions a company can expect its pre‐money Series A valuation to be between $2 million and $5 million, and that the VC will expect to purchase preferred stock that represents between 20% and 40% of the business, on a fully‐diluted basis.</p>
									<p>The problem arises if the valuation was miscalculated at the angel round.  In most cases, companies have overvalued the business, and this results in the angel investors and strategic partners suffering a significant loss in the paper value of their investments.  For example, if a company tells angels and partners that it has a $9 million valuation and then sells those angels $1 million of common stock, the angels will own 10% of the  company (post‐money) and will believe that their investment is worth $1 million going forward.  If, subsequently, the VCs value at half of that number (pre‐money), the angels will have to accept that they now own 10% of a $5 million company and the $1 million that they put in is now worth only $500,000.</p>
									<p>Another complication that can arise when selling equity to angels is that different angels and partners may request different valuations, which can result in a very complicated capitalization table that will be difficult to simplify at later stages of the business.</p>
									<p><strong>Nature of Equity.</strong><br />Selling equity to angels and partners means that founders are selling ownership of their company.  As shareholders, the angels will have certain rights and the company will owe them certain duties,  which cannot be waived.  The rights that these shareholders will have may make future sales of equity more difficult for the company (and in some instances, impossible).</p>
									<p><strong>Timing and Cost.</strong><br />In general, the legal documentation for a sale of equity by the company requires more paperwork and attorney time than a loan financing.  At a bare minimum, a stock purchase agreement is needed, as well as a shareholders agreement and some form of investor questionnaire.  Both the stock purchase agreement and shareholders agreement will need to be robust documents. It is likely that both of these agreements will need to be replaced with new documentation when a Series A round is raised.</p>
									<p>In addition, the company can lose a good deal of time trying to determine an appropriate valuation and then negotiating that valuation with the group of angels and partners, who (as stated above) may have different expectations as to what the final valuation should be.</p>
									<p class="clearfix">&nbsp;</p>
									
									<h4 class="color">Advantages of Convertible Loans.</h4>
									<p><strong>Valuation.</strong><br />In a convertible loan financing, the company issues convertible promissory notes to the investors and partners.  These notes are automatically converted into the same class of stock that is purchased in the Series A financing of the company.  This type of financing is commonly called a “bridge financing” because the goal is to raise sufficient capital to get the company from where it is today to a significant equity financing by an institutional VC.</p>
									<p>One very nice feature of convertible loan is that it avoids the valuation calculation, putting it off for the Series A financing, at which time the company will have better developed its business and can negotiate an accurate valuation with the VC investor.  The note holders will have their principal and interest converted into Series A stock at the same price per share negotiated for the Series A round or perhaps with a modest discount.  They have the benefit of “piggy‐backing” on the valuation negotiated by the VCs and the company has the advantage of going into those discussions with a more robust business.</p>
									<p>This type of financing should be more appealing to angel investors and strategic partners for a number of reasons.  First, they have a loan instrument, which is more secure than common stock.  Second, they will convert into preferred stock rather than common stock.  Third, they will convert both principal and interest, which will boost the number of shares that they receive.  In some instances, they are also converted as a discount to the actual Series A price, which will also boost their ownership.</p>
									<p>From the company’s standpoint, avoiding the valuation issue now puts the company in a better position to approach early‐stage VCs, and the immediate access to capital allows the company to focus on the initial phases of its business plan, which should work to increase the valuation that it ultimately negotiates.</p>
									<p><strong>Nature of loan.</strong><br />The company will have to acknowledge that it has loan on its balance sheet, but future investors will see that the loan will go away (i.e., be converted into equity) at the Series A financing, so it is much less worrisome.  loan holders do not have the same rights that shareholders have, and will not have any ability to impact the ongoing operations of the business.</p>
									<p><strong>Timing and Cost.</strong><br />By skipping the valuation discussion, the company will save time that would otherwise be spent negotiating with the various angels.  From a documentation standpoint, only a note purchase agreement, a form of convertible promissory note and an investor questionnaire are needed.  As compared to the equity documents, these are significantly simpler.  And this set of documents will not need to be replaced in their entirety in the future.</p>
